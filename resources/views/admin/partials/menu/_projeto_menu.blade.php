<h4>1. Como postar um projeto no site:</h4><br>
- Acesse o menu <strong>Projetos</strong> no menu lateral;<br>
- No canto superior direito da tabela, aperte no botão: <button class="btn btn-primary">Postar novo projeto</button><br>
- Preencha o formulário seguindo as seguintes regras e recomendações:<br><br>
<ul>
    <li>O campo Nome do Projeto e Autor do Projeto tem que conter menos de 255 caracteres;</li>
    <li>O campo Valor Mínimo e Valor Máximo deve receber apenas números, o próprio formulário formatará ele com a
        pontuação;</li>
    <li>O Valor Máximo deve ter um valor maior que valor mínimo;</li>
    <li>O Valor Máximo deve ter um valor maior que valor mínimo;</li>
    <li>O campo Descrição não tem um limite de caracteres, mas é recomendado que seja escrito uma pequena sinopse do
        projeto para melhorar a visualização final;</li>
    <li>O campo Mais detalhes, pode ser escrito da forma como você achar melhor, podendo usar letras em negrito, itálico
        e sublinhas em diferentes tamanhos de fonte;</li>
    <li>O campo Fotos do projeto só aceitará fotos em formato .jpg, .jpeg, .png e pode receber qualquer quantidade de
        fotos, porém uma única foto não pode ultrapassar o limite de 8MB de armazenamento, caso ultrapasse você receberá
        um alerta na tela e o formulário bloqueará o botão de publicar;</li>
    <li>É <strong class="text-danger">extremamente recomendado</strong> que ao selecionar as imagens, a última seja a 
        foto da capa. O sistema vai identificar automaticamente a última foto selecionada e carregada e a colocará como
        capa do projeto;</li>
    <li>É recomendado começar a preencher o formulário de projeto pela foto, pois dependendo da quantidade de foto e da
        qualidade da internet no momento, o servidor pode demorar pra receber essas fotos e se o formulário for enviado
        e as fotos forem incompletas, o sistema não vai cadastrar o projeto.</li>
</ul>
- Caso alguma das regras seja violada você receberá um alerta em vermelho no topo da tela informando que alguma regra do
formulário foi violada;<br>
- Se você preencheu tudo corretamente no formulário, você receberá um alerta verde no topo da tela confirmando a
postagem do projeto;<br>
- Se você preencheu tudo no formulário e ao apertar em publicar não receber nenhum alerta no topo tela e ele não está na
tabela de projetos, significa que sua publicação caiu em um erro não tratado do sistema, nesse caso tente novamente e
caso aconteça novamente logo em seguida, abra um chamado para a equipe de desenvolvimento entrar em ação;<br>
- Ao publicar, seu projeto estará automaticamente postado no topo da tela de Projetos no site principal do Projetos STI
e visível para todos com as informações que você preencheu na criação e você será redirecionado para a tela de edição do
projeto que você acabou de postar;<br><br>
<h4>2. Como editar um projeto postado:</h4><br>
- Acesse o menu <strong>Projetos</strong> no menu lateral;<br>
- No centro da tela, se você tiver um projeto postado, ele aparecerá numa tabela;<br>
- A última coluna da tabela, aperte no lápis ( <i class="bi bi-pencil-fill"></i> ) que representa a edição daquele
projeto postado;<br>
- No formulário que será carregado, altere as informações necessárias;<br>
- <strong>ATENTE-SE AS REGRAS DO FORMULÁRIO PASSADAS NO TUTORIAL DE CRIAÇÃO!</strong><br>
- Após alterar as informações necessárias, basta apertar no botão <button class="btn btn-primary">Atualizar</button> no
final do formulário;<br>
- Assim como na criação, se tudo estiver dentro dos conformes, um alerta verde aparecerá no topo da tela;<br><br>
<h4>3. Como alterar as fotos de um projeto:</h4><br>
- Ainda dentro do formulário de edição, você verá uma tabela com uma miniatura da foto publicada junto daquele
projeto;<br>
- Para remover uma foto, basta apertar uma vez no botão <button class="btn btn-danger">Excluir</button> na linha da foto
na tabela;<br>
- Para adicionar uma foto ou mais, apenas insira-as no formulário e aperte em <button
class="btn btn-primary">Atualizar</button><br>
- Caso seja necessário alterar as imagens do carrossel de imagens na página do projeto, primeiro
<strong class="text-danger">exclua a foto de capa atual </strong>, suba as novas imagens pro carrossel e carregue
novamente a foto de capa;<br>
- <strong>ATENTE-SE AS REGRAS PARA ADICIONAR FOTOS NO TUTOTIAL DE CRIAÇÃO!</strong><br><br>
<h4>4. Como excluir um projeto:</h4><br>
- Acesse o menu <strong>Projetos</strong> no menu lateral;<br>
- Na última coluna da tabela de projetos, ao lado do lápis, você verá um ícone de lixo ( <i
    class="text-danger bi bi-trash-fill"></i> );<br>
- Ao apertar, um alerta de confirmação aparecerá no meio da tela para confirmar sua ação;<br>
- <strong>CUIDADO AO PROSSEGUIR, POIS ESSA AÇÃO É IRREVERSÍVEL</strong>