<h4>1. Como adicionar um membro a equipe:</h4><br>
- Acesse o menu <strong>Equipe</strong> no menu lateral;<br>
- No canto superior direito da tabela, aperte no botão: <button class="btn btn-primary">Novo membro</button><br>
- Preencha o formulário seguindo as seguintes regras e recomendações:<br><br>
<ul>
    <li>O campo Nome Completo tem que conter menos de 255 caracteres;</li>
    <li>O campo Telefone deve receber apenas números, o próprio formulário formatará ele com os caracteres especiais;
    </li>
    <li>O campo Bio deve conter menos de 100 caracteres e deve ser um descrição bem simples sobre a pessoa;</li>
    <li>Os campos LinkedIn e Instagram, podem receber os links para as redes sociais da pessoa respectivamente, e também
        podem ser nulas/vazias;</li>
    <li>O campo Descrição não tem um limite de caracteres, mas recomendo não se prolongar tanto tendo em vista que o
        card de usuário no layout principal do sistema não é muito espaçoso;</li>
    <li>O campo Foto só aceitará UMA foto em formato .jpg, .jpeg, .png. A foto não pode ultrapassar o limite de 8MB de
        armazenamento, caso ultrapasse você receberá um alerta na tela e o formulário bloqueará o botão de cadastrar;
    </li>
    <li>É recomendado começar a preencher o formulário de cadastro pela foto, pois dependendo da qualidade da foto e da
        internet no momento, o servidor pode demorar pra receber essa foto e se o formulário for enviado e a foto chegar
        incompleta, o sistema não vai cadastrar o membro da equipe.</li>
</ul>
- Caso alguma das regras seja violada você receberá um alerta em vermelho no topo da tela informando que alguma regra do
formulário foi violada;<br>
- Se você preencheu tudo corretamente no formulário, você receberá um alerta verde no topo da tela confirmando o
cadastro do membro na equipe;<br>
- Se você preencheu tudo no formulário e ao apertar em cadastrar não receber nenhum alerta no topo tela e ele não está
na tabela de membros da equipe, significa que seu cadastro caiu em um erro não tratado do sistema, nesse caso tente
novamente e caso aconteça novamente logo em seguida, abra um chamado para a equipe de desenvolvimento entrar em
ação;<br>
- Ao cadastrar, a pessoa cadastrada estará automaticamente na tela de Equipe no site principal do Projetos STI e visível
para todos com as informações que você preencheu no cadastro;<br><br>
<h4>2. Como editar um membro da equipe:</h4><br>
- Acesse o menu <strong>Equipe</strong> no menu lateral;<br>
- No centro da tela, se você tiver uma pessoa cadastrada, ele(a) aparecerá numa tabela;<br>
- A última coluna da tabela, aperte no lápis ( <i class="bi bi-pencil-fill"></i> ) que representa a edição daquele
membro da equipe;<br>
- No formulário que será carregado, altere as informações necessárias;<br>
- <strong>ATENTE-SE AS REGRAS DO FORMULÁRIO PASSADAS NO TUTORIAL DE CADASTRO!</strong><br>
- Após alterar as informações necessárias, basta apertar no botão <button class="btn btn-primary">Atualizar</button> no
final do formulário;<br>
- Assim como no cadastro, se tudo estiver dentro dos conformes, um alerta verde aparecerá no topo da tela;<br><br>
<h4>3. Como alterar a foto de um membro:</h4><br>
- Ainda dentro do formulário de edição, você verá uma miniatura da foto da pessoa;<br>
- Para alterar a foto, basta usar o campo abaixo da foto atual e escolher uma nova foto, e apertar em <button
    class="btn btn-primary">Atualizar</button><br>
- A foto será automaticamente adicionada e antiga removida, e já será possível visualizar a nova foto dentro do
formulário de edição;<br>
- <strong>ATENTE-SE AS REGRAS PARA ADICIONAR FOTO NO TUTOTIAL DE CADASTRO!</strong><br><br>
<h4>4. Como excluir um membro da equipe:</h4><br>
- Acesse o menu <strong>Equipe</strong> no menu lateral;<br>
- Na última coluna da tabela de membros da equipe, ao lado do lápis, você verá um ícone de lixo ( <i
    class="text-danger bi bi-trash-fill"></i> );<br>
- Ao apertar, um alerta de confirmação aparecerá no meio da tela para confirmar sua ação;<br>
- <strong>CUIDADO AO PROSSEGUIR, POIS ESSA AÇÃO É IRREVERSÍVEL</strong>